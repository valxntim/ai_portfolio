"""
AI Portfolio Backend API
------------------------
This FastAPI service acts as the orchestration layer for the AI Portfolio.
It handles semantic search (RAG) via Supabase pgvector, LLM inference via Groq,
and enforces deterministic structured outputs using Pydantic and a self-correction retry loop.

Author: Gustavo Valentim
"""

import os
import logging
import httpx
from dotenv import load_dotenv
from fastapi import FastAPI, HTTPException, Request, BackgroundTasks
from pydantic import BaseModel, Field, ValidationError
from supabase import create_client
from openai import OpenAI
from groq import Groq
from fastapi.middleware.cors import CORSMiddleware
from slowapi import Limiter, _rate_limit_exceeded_handler
from slowapi.util import get_remote_address
from slowapi.errors import RateLimitExceeded

# Set up internal logging for monitoring production errors
logging.basicConfig(level=logging.INFO)
logger = logging.getLogger(__name__)

# Load environment variables (API keys are NEVER hardcoded)
load_dotenv()

# Initialize API Clients
supabase = create_client(os.getenv("SUPABASE_URL"), os.getenv("SUPABASE_KEY"))
client = OpenAI(api_key=os.getenv("OPENAI_API_KEY"))
groq_client = Groq(api_key=os.getenv("GROQ_API_KEY"))

# Initialize Rate Limiter (IP-based) to protect cloud resources
limiter = Limiter(key_func=get_remote_address)

app = FastAPI(title="Gustavo Portfolio API - Production")
app.state.limiter = limiter
app.add_exception_handler(RateLimitExceeded, _rate_limit_exceeded_handler)

# ==========================================
# SECURITY: Strict CORS Configuration
# ==========================================
# We only allow requests explicitly originating from the Vercel frontend.
# This prevents unauthorized domains from hijacking the API and burning LLM credits.
app.add_middleware(
    CORSMiddleware,
    allow_origins=["https://gustavo-valentim.vercel.app"], 
    allow_credentials=True,
    allow_methods=["*"],
    allow_headers=["*"],
)

# ==========================================
# DATA MODELS (Pydantic Validation Layer)
# ==========================================
class ChatMessage(BaseModel):
    role: str
    content: str

class QueryRequest(BaseModel):
    question: str = Field(..., max_length=500, description="The user's question")
    history: list[ChatMessage] = [] 

class QueryResponse(BaseModel):
    """The standard format sent back to the React frontend."""
    answer: str
    sources: list[str]

class LLMResponse(BaseModel):
    """
    Strict schema to enforce deterministic output from the LLM.
    If the LLM hallucinates a different JSON structure, Pydantic will raise a ValidationError.
    """
    answer: str = Field(..., description="The answer generated by the LLM based on the context and question.")

class ContactRequest(BaseModel):
    name: str = Field(..., max_length=100)
    email: str = Field(..., max_length=150)
    message: str = Field(..., max_length=1000)


# ==========================================
# HELPER FUNCTIONS
# ==========================================
def embed_query(text: str):
    """Generates a vector embedding for the user's question using OpenAI."""
    response = client.embeddings.create(
        model="text-embedding-3-small",
        input=text
    )
    return response.data[0].embedding

def send_notification_email(name: str, user_email: str, message: str):
    """Asynchronously sends an email notification via the Resend API."""
    resend_api_key = os.getenv("RESEND_API_KEY")
    my_email = os.getenv("EMAIL_USER") 

    if not resend_api_key or not my_email:
        logger.warning("Resend API key missing. Contact saved to DB, but email skipped.")
        return

    headers = {
        "Authorization": f"Bearer {resend_api_key}",
        "Content-Type": "application/json"
    }

    payload = {
        "from": "Portfolio Agent <onboarding@resend.dev>",
        "to": my_email,
        "subject": f"🚀 New Portfolio Contact: {name}",
        "html": f"""
        <h2>You have a new message from your portfolio!</h2>
        <p><strong>Name:</strong> {name}</p>
        <p><strong>Email:</strong> {user_email}</p>
        <p><strong>Message:</strong></p>
        <blockquote style="border-left: 4px solid #ccc; padding-left: 10px;">{message}</blockquote>
        <br>
        <p><em>Note: Just click 'Reply' to email them back directly!</em></p>
        """,
        "reply_to": user_email
    }

    try:
        with httpx.Client() as http_client:
            response = http_client.post("https://api.resend.com/emails", json=payload, headers=headers)
            response.raise_for_status()
            logger.info("Email sent successfully via Resend API!")
    except Exception as e:
        logger.error(f"Failed to send email via Resend: {e}")


